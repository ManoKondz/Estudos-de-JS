/*
    + -> soma
    - -> subtração
    / -> divisão
    * -> multiplicação
    % -> Resto
    ++ -> Incremento 1
    -- -> Decremento 1
    += -> Incremento (?)valor
    -+ -> Decremento (?)valor
*/

let num=0, num2, num3, res=0;

num2=num3=10;

res=(num+num2)*num3;

console.log(num, num2, num3, res);

res++
num3--

console.log(res, num3)

res+=5
num3-=5

console.log(res, num3)
